// JavaScript to create typing animation

const heading = document.querySelector('.typing-animation');
const textToType = "Welcome to SuperHero Hunter";
let charIndex = 0;

function type() {
    if (charIndex < textToType.length) {
        heading.textContent += textToType.charAt(charIndex);
        charIndex++;
        setTimeout(type, 100); // Adjust typing speed here (e.g., 100ms for faster typing)
    }
}

